    function connect_bd(){
        $server = "localhost";
        $bd = "nombre_bd";
        $usu = "usuario";
        $pass = "password";

        if (!($link=mysql_connect($server, $usu, $pass))){
            echo "Error conectando a la base de datos.";
            exit();
        }

        if (!mysql_select_db($bd, $link)){
            echo "Error seleccionando la base de datos.";
            exit();
        }

        return $link;
    }

    function generalSelect($sql){
        $link = connect_bd();
        $res = mysql_query($sql, $link) or die (mysql_error());
        $var = mysql_fetch_array($res);
        mysql_close($link);
        return $var;
    }

